/**
 * Generate Google Fonts URL for a specific font
 */
export function getGoogleFontUrl(fontName, weights = null) {
  const font = googleFonts[fontName];
  if (!font) return null;
  
  const fontFamily = fontName.replace(/ /g, '+');
  const fontWeights = weights || font.weights;
  const weightsString = fontWeights.join(';');
  
  return `https://fonts.googleapis.com/css2?family=${fontFamily}:wght@${weightsString}&display=swap`;
}

/**
 * Generate Google Fonts URL for multiple fonts
 */
export function getGoogleFontsUrl(fontList) {
  const families = fontList.map(({ name, weights }) => {
    const font = googleFonts[name];
    if (!font) return null;
    
    const fontFamily = name.replace(/ /g, '+');
    const fontWeights = weights || font.weights;
    const weightsString = fontWeights.join(';');
    
    return `family=${fontFamily}:wght@${weightsString}`;
  }).filter(Boolean);
  
  if (families.length === 0) return null;
  
  return `https://fonts.googleapis.com/css2?${families.join('&')}&display=swap`;
}

/**
 * Get all fonts by category
 */
export function getFontsByCategory(category) {
  return Object.entries(googleFonts)
    .filter(([_, config]) => config.category === category)
    .map(([name, config]) => ({ name, ...config }));
}

/**
 * Search fonts by name
 */
export function searchFonts(query) {
  const searchTerm = query.toLowerCase();
  return Object.entries(googleFonts)
    .filter(([name]) => name.toLowerCase().includes(searchTerm))
    .map(([name, config]) => ({ name, ...config }));
}

